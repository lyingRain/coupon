/**
 * <h1>应用启动入口</h1>
 * @author zzxstart
 * @date 2020/5/3 - 10:10
 */
@SpringBootApplication
@EnableEurekaServer
public class EurekaApplication {

    public static void main(String[] args) {
        SpringApplication.run(EurekaApplication.class,args);
    }
}
